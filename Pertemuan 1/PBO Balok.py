print("Aplikasi menghitung luas dan volume balok")

# Atur Nilai Variabel
panjang = 27
lebar = 17
tinggi = 7

# Rumus
luas = (2 * panjang * lebar) + (2 * panjang * tinggi) + (2 * lebar * tinggi)
volume = panjang * lebar * tinggi

# Output
print("Panjang = ",panjang)
print("Lebar = ",lebar)
print("Tinggi = ",tinggi)
print("Luas = ",luas)
print("Volume = ",volume)